#    SCOPE / LA PORTEE   



# La portée est l'idée en programmation que certaines variables
#    sont accessibles/inaccessibles depuis d'autres parties du programme.

# Les blocs sont des instructions qui existent entre des accolades {}.

# La portée globale fait référence au contexte dans lequel les variables
#    sont accessibles à chaque partie du programme. 

# Les variables globales sont des variables qui existent dans la portée globale.

# La portée du bloc fait référence au contexte dans lequel les variables qui ne
#    sont accessibles que dans le bloc où elles sont définies.

# Les variables locales sont des variables qui existent dans la portée du bloc.

# L'espace de noms global est l'espace dans notre code qui contient
#    des informations de portée globale. 

# La pollution de la portée se produit lorsque trop de variables existent dans
#    un espace de noms ou que les noms de variables sont réutilisés.