#        VAR

# le nom ne doit pas commencer par un chiffre
# myName != myname (sensible casse)
# pas de nom de var avec mot clé

var myName ="cedric";


#---
#        LET

# let => la variable peut etre reaffecté
# si vide undefined

let myName ="cedric";
# cedric
myName ="alex";
# alex


#---
#       CONST

# ne peut pas etre reaffecté => error

const myName ="ced";