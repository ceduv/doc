#    SCOPE / LA PORTEE   


La portée                   # accessibles/inaccessibles depuis d'autres parties du programme.

Les blocs                   # instructions qui existent entre des accolades {}.

La portée globale           # contexte accessibles à chaque partie du programme. 

Les variables globales      # variables qui existent dans la portée globale.

La portée du bloc           # dans le bloc où elles sont définies.

Les variables locales       # existent dans la portée du bloc.

L espace de noms global     # espace qui contient des informations de portée globale.     

La pollution de la portée   # trop de variables existent dans un espace de
                            # noms ou que les noms de variables sont réutilisés.